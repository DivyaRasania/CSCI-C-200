def toThePower(num1, num2):
    '''
    number, number -> number
    
    purpose: power the number to itself
    '''
    return num1 ** num2

def quadruplicate(str1):
    '''
    string -> string

    purpose: copies the string 4 times
    '''
    return str1 * 4

def subtract(num1, num2):
    '''
    number, number -> number

    purpose: subtract 2 numbers
    '''
    return num1 - num2

def multiplicate(str1, num1):
    '''
    string, number -> string

    purpose: copies the string x number of times
    '''
    return str1 * num1

